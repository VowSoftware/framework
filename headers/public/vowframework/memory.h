#ifndef VOW_GUARD_MEMORY
#define VOW_GUARD_MEMORY

////////////////////////////////////////////////////////////////////////////////
// Dynamically allocates memory and zero-initializes it.
//
// Parameters
//   * `bytes`: Amount of bytes to allocate.
//
// Returns a pointer to the allocated memory.
//
// Asserts
//   * Memory was successfully allocated.
////////////////////////////////////////////////////////////////////////////////
void* vow_memory_allocate(int bytes);

////////////////////////////////////////////////////////////////////////////////
// Frees dynamically allocated memory.
//
// Parameters
//   * `memory`: Pointer to the memory to free.
////////////////////////////////////////////////////////////////////////////////
void vow_memory_free(void* memory);

#endif