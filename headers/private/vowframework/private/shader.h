#ifndef VOW_GUARD_SHADER_PRIVATE
#define VOW_GUARD_SHADER_PRIVATE

#include <vowframework/handle.h>

////////////////////////////////////////////////////////////////////////////////
// Initializes the shader module.
//
// This function should be called by `vow_framework_initalize()`.
////////////////////////////////////////////////////////////////////////////////
void vow_shader_initialize(void);

////////////////////////////////////////////////////////////////////////////////
// Finalizes the shader module.
//
// This function should be called by `vow_framework_finalize()`.
////////////////////////////////////////////////////////////////////////////////
void vow_shader_finalize(void);

////////////////////////////////////////////////////////////////////////////////
// Updates the shader module.
//
// This function should be called by `vow_framework_update()`.
////////////////////////////////////////////////////////////////////////////////
void vow_shader_update(float frameTime);

////////////////////////////////////////////////////////////////////////////////
// Binds a shader to the OpenGL context.
//
// Parameters
//   * `handle`: The shader's handle.
////////////////////////////////////////////////////////////////////////////////
void vow_shader_bind(VowHandle handle);

////////////////////////////////////////////////////////////////////////////////
// Unbinds a shader from the OpenGL context.
////////////////////////////////////////////////////////////////////////////////
void vow_shader_unbind(void);

#endif